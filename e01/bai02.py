# 1. Input
# Nhập tuổi vào 3 biến: dad, mom, me
dad = int(input("Nhap tuoi cha: "))
mom = int(input("Nhap tuoi me: "))
me = int(input("Nhap tuoi ban: "))

# 2. Process
# Tính tuổi trung binh: tuoi_tb = (dad + mom + me) / 3
tuoi_tb = (dad + mom + me) / 3

# 3. Output
# Xuất biến tuoi_tb
print("Tuoi TB cua 3 nguoi la:", tuoi_tb)

